CREATE TABLE IF NOT EXISTS folders (
    NumID INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    id TEXT NOT NULL,
    name TEXT NOT NULL
);

CREATE TABLE notes (
    NumID INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    id TEXT NOT NULL,
    name TEXT NOT NULL,
    modified TIMESTAMP DEFAULT now() NOT NULL,
    folderIdNum INTEGER REFERENCES folders(NumID) NOT NULL,
    folderId TEXT NOT NULL,
    content TEXT NOT NULL
);